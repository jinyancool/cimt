embedding_jitter <- function(sub_mask_embedding, jitter_features_highlight, pct = 0.1){
  jitter_features <- base::intersect(jitter_features_highlight, colnames(sub_mask_embedding))
  sub_mask_embedding$group_jitter <- 1:nrow(sub_mask_embedding)
  sub_mask_embedding$jitter_flag <- "before"
  sub_mask_embedding_jitted <- sub_mask_embedding
  for(i in jitter_features){
    feature_i <- sub_mask_embedding_jitted[[i]]
    if(length(unique(feature_i)) < 10){
      # maxf <- max(feature_i, na.rm = T)
      sub_mask_embedding_jitted[[i]] <- feature_i + 1
      # sub_mask_embedding_jitted[[i]][sub_mask_embedding_jitted[[i]] > maxf] <- NA
    }else{
      sub_mask_embedding_jitted[[i]] <- feature_i + median(feature_i) * pct
    }
  }
  sub_mask_embedding_jitted$jitter_flag <- "after"
  rbind(sub_mask_embedding,sub_mask_embedding_jitted)
}

