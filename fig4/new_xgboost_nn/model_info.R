sim_corfeature <- function(tb_now, tb_next, w = 1.5){
  tb_now_sub <- tb_now$used_feature[[1]]
  feature_sim <- lapply(tb_next$used_feature, function(.l){
    length(base::intersect(.l, tb_now_sub))/length(base::union(.l, tb_now_sub))
  }) %>% unlist()
  cor_sim <- tb_next$mean_R
  feature_sim + (cor_sim * w)
}
main_testone <- function(model_list, df, jx, strats_value, main_value, width){
  model_i <- model_list[[jx]]
  stv <- strats_value[jx]
  ft1 <- df[[main_value]] >= stv
  if(jx == length(strats_value)){
    ft2 <- T
  }else{
    ft2 <- df[[main_value]] <= (stv + width)
  }
  
  if(any(ft1 & ft2)){
    ft_df <- df %>% dplyr::filter(ft1 & ft2)
    usef0 <- model_i$feature_names
    df0 <- ft_df %>% dplyr::select(all_of(usef0)) 
    tibble(
      index = ft_df$test_index,
      true = ft_df$max_data,
      age = ft_df$age,
      gender = ft_df$gender_level,
      preidct = predict(model_i, df0 %>% as.matrix())
    )
  }else{
    NULL
  }
}
main_predict <- function(model_list,test_df, jx, strats_value, main_value, width){
  pddf <- lapply(1:length(model_list), function(jx){
    main_testone(model_list,test_df, jx, strats_value, main_value, width)
  }) %>% list_rbind() %>% 
    group_by(index) %>% 
    summarise(preidct = mean(preidct), true = mean(true), age = mean(age), gender = gender[1])
  pddf
}
mask_feature <- function(mask_embedding, used_features_table,all_used_feature, arrow = F){
  if(!arrow){
    mean_embedding <- mask_embedding
    for(i in 1:nrow(mask_embedding)){
      agex <- mask_embedding$age[[i]]
      ft1 <- agex >= used_features_table$start_age
      ft2 <- agex <= used_features_table$end_age
      used_features_real <- used_features_table[ft1 & ft2,]$used_features %>% unlist() %>% unique()
      delf <- base::setdiff(all_used_feature, used_features_real)
      
      mask_embedding[i,delf] <- NA
      mean_embedding[i,used_features_real] <- NA
    }
  }else{
    mask_embedding1 <- mask_embedding %>% dplyr::filter(jitter_flag == "before")
    mean_embedding <- mask_embedding %>% dplyr::filter(jitter_flag == "before")
    for(i in 1:nrow(mask_embedding1)){
      agex <- mask_embedding1$age[[i]]
      ft1 <- agex >= used_features_table$start_age
      ft2 <- agex <= used_features_table$end_age
      used_features_real <- used_features_table[ft1 & ft2,]$used_features %>% unlist() %>% unique()
      delf <- base::setdiff(all_used_feature, used_features_real)
      
      ftl <- mask_embedding$group_jitter == i
      mask_embedding[ftl,delf] <- NA
      ftls <- mean_embedding$group_jitter == i
      mean_embedding[ftls,used_features_real] <- NA
    }
  }
  
  impute_mean <- lapply(names(mean_embedding), function(cn){
    if(sum(is.na(mean_embedding[[cn]])) > 0){
      setNames(mean(mean_embedding[[cn]], na.rm = T), cn)
    }
  }) %>% unlist()
  
  for(cn in names(mean_embedding)){
    if(sum(is.na(mask_embedding[[cn]])) > 0){
      mask_embedding[[cn]][is.na(mask_embedding[[cn]])] <- impute_mean[cn]
    }
  }
  mask_embedding
}
